static uint16_t crc16(uint8_t *buffer, uint16_t buffer_length)
{
    uint8_t crc_hi = 0xFF; /* high CRC byte initialized */
    uint8_t crc_lo = 0xFF; /* low CRC byte initialized */
    unsigned int i; /* will index into CRC lookup */

    /* pass through message buffer */
    while (buffer_length--) {
        i = crc_hi ^ *buffer++; /* calculate the CRC  */
        crc_hi = crc_lo ^ table_crc_hi[i];
        crc_lo = table_crc_lo[i];
    }
    return (crc_hi << 8 | crc_lo);
}

/* Builds a RTU request header */
int _rtu_build_request_header(small_modbus_t *smb,uint8_t *buff,int slave,int fun,int reg,int num)
{
    buff[0] = slave;
    buff[1] = fun;
    buff[2] = reg >> 8;
    buff[3] = reg & 0x00ff;
    buff[4] = num >> 8;
    buff[5] = num & 0x00ff;
    return _MODBUS_RTU_PRESET_REQ_LENGTH;
}

/* Builds a RTU response header */
int _rtu_build_response_header(small_modbus_t *smb,uint8_t *buff,int slave,int fun)
{
    buff[0] = slave;
    buff[1] = fun;
    return _MODBUS_RTU_PRESET_RSP_LENGTH;
}

int _rtu_check_send_pre(small_modbus_t *smb,uint8_t *buff,int length)
{
    uint16_t crc = crc16(buff,length);
    buff[length++] = crc >> 8;
    buff[length++] = crc & 0x00FF;
    return length;
}

int _rtu_check_wait_poll(small_modbus_t *smb,uint8_t *buff,int length)
{
    int addr = buff[0];
    if (addr != smb->slave_addr && addr != MODBUS_BROADCAST_ADDRESS)
    {
        smb->port->debug(smb,0,"slave adrr: %d is err\n",addr);
        return MODBUS_FAIL;
    }

    uint16_t crc_cal = crc16(buff, length - 2);
    uint16_t crc_recv = (buff[length - 2] << 8) | buff[length - 1];
    if (crc_cal == crc_recv)
    {
       return length;
    }
    smb->port->debug(smb,0,"crc  0x%0X != 0x%0X\n",crc_cal, crc_recv);
    return MODBUS_FAIL;
}

int _rtu_check_wait_confirm(small_modbus_t *smb,uint8_t *buff,int length)
{
    int addr = buff[0];
    if (addr != smb->slave_addr && addr != MODBUS_BROADCAST_ADDRESS)
    {
        smb->port->debug(smb,0,"slave adrr: %d is err\n", addr);
        return MODBUS_FAIL;
    }
    return MODBUS_OK;
}

